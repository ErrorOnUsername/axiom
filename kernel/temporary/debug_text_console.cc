void TextConsole::put_char(char c)
{
	switch(c) {
		case ' ':
			column++;
			break;
		case '\t':
			column += TAB_WIDTH;
			break;
		case '\r':
			column = 0;
		case '\n':
			row++;
			column = 0;
		default:
			draw_char(c);
	}
}

